#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		ft_putchar_fd('-', fd);
	}
	if (n / 10 >= 1)
		ft_putnbr_fd((n / 10), fd);
	c = n % 10 + '0';
	ft_putchar_fd(c, fd);
	return ;
}
